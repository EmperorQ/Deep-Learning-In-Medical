        # shift_bboxes = list()
        # for bbox in bboxes:
        #     shift_bboxes.append([bbox[0] + x, bbox[1] + y, bbox[2] + x, bbox[3] + y])
        boxes = bboxes.copy()
        boxes[:, :2] += (int(x), int(y))
        boxes[:, 2:] += (int(x), int(y))

        # for boxes in bboxes:
        #     boxes[0] = boxes[0] + x
        #     boxes[1] = boxes[1] + y
        #     boxes[2] = boxes[2] + x
        #     boxes[3] = boxes[3] + y
